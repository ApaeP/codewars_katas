# Codewars - Kata Solution (Ruby)
# REMOVE DUPLICATES FROM LIST (8 kyu)

# Instructions
# Define a function that removes duplicates from an array of numbers and returns it as a result.
# The order of the sequence has to stay the same.

# Solution
def distinct(seq)
  seq.uniq
end


