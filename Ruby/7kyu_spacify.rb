# Codewars - Kata Solution (Ruby)
# SPACIFY (7 kyu)

# Instructions
# Modify the spacify function so that it returns the given string with spaces inserted between each character.
# spacify("hello world") # returns "h e l l o   w o r l d"

# Solution
def spacify(str)
  str.chars.join(' ')
end


